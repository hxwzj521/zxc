package com.cn.pojo;

public class Items {

    private String name;

    private float price;

    private String detail;

    public String getName() {
        return name;
    }

    public float getPrice() {
        return price;
    }

    public String getDetail() {
        return detail;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
